    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        kursiyerveritabani kursiyer = new kursiyerveritabani();
        String TC = tf_ekletcno.getText();
        String adayadi = tf_ekleadi.getText();
        String adaysoyadi = tf_eklesoyadi.getText();
        String aractip = "";
        String saglikraporu = "";
        if (jRadioButton3.isSelected())
            aractip = "Manuel";
        else if(jRadioButton4.isSelected())
            aractip = "Otomatik";
        String telefon = tf_ekletelefonno.getText();
        if (jRadioButton1.isSelected())
            saglikraporu = "Var";
        else if(jRadioButton2.isSelected())
            saglikraporu = "Yok";
        String ehliyetsinifi = cb_ekleehliyetsinifi.getSelectedItem().toString();
        String sinifismi = cb_eklesinif.getSelectedItem().toString();
        if ("".equals(TC) || "".equals(adayadi)||"".equals(adaysoyadi)||"".equals(aractip)||"".equals(telefon)||"".equals(saglikraporu)||"".equals(ehliyetsinifi)||"".equals(sinifismi)) {
            JOptionPane.showMessageDialog(null, "Lütfen Tüm Boşlukları Doldurunuz");
        }
        else{
            
            kursiyer.ekle(TC, adayadi, adaysoyadi, aractip, telefon, saglikraporu, ehliyetsinifi, sinifismi);
            cb_eklesinifActionPerformed(evt);
        }
        String durum = "";
        int toplamtaksit = 0;
        int odenentaksit = 0;
        String odemetipi = "";
        if (jRadioButton17.isSelected()){
            durum = "Odendi";
            odemetipi = "Nakit";
        }
        else if(jRadioButton18.isSelected()){
            durum = "Odenmedi";
            odemetipi = "Taksit";
            if (jRadioButton19.isSelected())
                toplamtaksit = 3;
            if (jRadioButton20.isSelected())
                toplamtaksit = 6;
            if (jRadioButton21.isSelected())
                toplamtaksit = 9;
            if (jRadioButton22.isSelected())
                toplamtaksit = 12;
        }
        kursiyer.odemeekle(TC, odemetipi, toplamtaksit, odenentaksit, durum);
        listele();
    }//GEN-LAST:event_jButton1ActionPerformed

