    public void odemebilgigetir(){
        kursiyerveritabani kursiyer = new kursiyerveritabani();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int selectedRowIndex = jTable1.getSelectedRow();
        jLabel17.setText(model.getValueAt(selectedRowIndex, 1).toString());
        jLabel18.setText(model.getValueAt(selectedRowIndex, 2).toString());
        jLabel19.setText(model.getValueAt(selectedRowIndex, 3).toString());
        jLabel20.setText(model.getValueAt(selectedRowIndex, 9).toString());
        ArrayList odemedurum = new ArrayList();
        odemedurum = kursiyer.odemedurumgetir(model.getValueAt(selectedRowIndex, 1).toString());
        jLabel23.setText(odemedurum.get(2).toString());
        if ((int)odemedurum.get(1) == (int)odemedurum.get(0)) {
            kursiyer.odemedurumguncelle(model.getValueAt(selectedRowIndex, 1).toString());
            jLabel23.setText("Odendi");
        }
        
        if (jLabel23.getText().equals("Odenmedi")) {
            jLabel29.setText(Integer.toString((int)odemedurum.get(0) - (int)odemedurum.get(1)));
            jComboBox1.removeAllItems();
            jPanel6.setVisible(true);
            for (int i = 1; i <= (int)odemedurum.get(0) - (int)odemedurum.get(1); i++) {
                jComboBox1.addItem(Integer.toString(i));
            }
        }
        else{
            jPanel6.setVisible(false);
        }
    }
