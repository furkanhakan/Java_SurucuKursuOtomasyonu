    public void listele(){
        ArrayList kursiyerler = new ArrayList();
        kursiyerveritabani kursiyer = new kursiyerveritabani();
        kursiyerler = kursiyer.listele();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        DefaultTableModel model2 = (DefaultTableModel) jTable2.getModel();
        DefaultTableModel model3 = (DefaultTableModel) jTable3.getModel();
        for (int i = 0; i < model.getRowCount();) {
            model.removeRow(0);
        }
        for (int i = 0; i < model2.getRowCount();) {
            model2.removeRow(0);
        }
        for (int i = 0; i < model3.getRowCount();) {
            model3.removeRow(0);
        }
        Object[] dizi = new Object[10];
        int index = 0;
        for (int i = 0; i < kursiyerler.size()-9; i+=10) {
            index = 0;
            for (int j = i; j < i+10; j++) {
                dizi[index] = kursiyerler.get(j);
                index++;
            }
            model.insertRow(model.getRowCount(),dizi);
            model2.insertRow(model2.getRowCount(),dizi);
        }
        ArrayList siniflar;
        siniflar = new ArrayList();
        Object[] dizi2 = new Object[5];
        siniflar = kursiyer.tumsiniflarigetir();
        System.out.println(siniflar.size());
        for (int i = 0; i < siniflar.size()-4; i+=5) {
            index = 0;
            for (int j = i; j < i+5; j++) {
                dizi2[index] = siniflar.get(j);
                index++;
            }
            model3.insertRow(model3.getRowCount(), dizi2);
        }
        
        
    }
    
